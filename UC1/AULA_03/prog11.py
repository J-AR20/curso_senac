# Vamos falar sobre Estruturas de Repetição - while & for

# função 'while' - mais adequado para estruturas infinitas
cont = 0
while cont <= 5:
    print(cont)
    cont = cont + 1



# função 'for' - serve para estruturas de repetição finitas (aquela que tem um certo número de repetições), bom para listas
for cont2 in  range(6):
    print(cont2)

