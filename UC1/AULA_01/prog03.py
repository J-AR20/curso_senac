# Escreva um programa que leia um valor inteiro e apresente os resultados do quadrado e do cubo do valor lido

valor = int(input("Me informe um número: "))
qd = valor**2
cb = valor**3
print("O número", valor, "elevado ao quadrado é", str(qd), "e elevado ao cubo é", str(cb))

# No Python o operador exponencial que é utilizado é '**', quando em outras linguagens, a exemplo do R se utiliza o '^'